import { ComponentFixture, TestBed } from '@angular/core/testing';
import { of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, convertToParamMap } from '@angular/router';

import { ApiendpointTestModule } from '../../../test.module';
import { ApiEndpointComponent } from 'app/entities/api-endpoint/api-endpoint.component';
import { ApiEndpointService } from 'app/entities/api-endpoint/api-endpoint.service';
import { ApiEndpoint } from 'app/shared/model/api-endpoint.model';

describe('Component Tests', () => {
  describe('ApiEndpoint Management Component', () => {
    let comp: ApiEndpointComponent;
    let fixture: ComponentFixture<ApiEndpointComponent>;
    let service: ApiEndpointService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [ApiendpointTestModule],
        declarations: [ApiEndpointComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: of({
                defaultSort: 'id,asc',
              }),
              queryParamMap: of(
                convertToParamMap({
                  page: '1',
                  size: '1',
                  sort: 'id,desc',
                })
              ),
            },
          },
        ],
      })
        .overrideTemplate(ApiEndpointComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(ApiEndpointComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(ApiEndpointService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ApiEndpoint(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.apiEndpoints && comp.apiEndpoints[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new ApiEndpoint(123)],
            headers,
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.apiEndpoints && comp.apiEndpoints[0]).toEqual(jasmine.objectContaining({ id: 123 }));
    });

    it('should calculate the sort attribute for an id', () => {
      // WHEN
      comp.ngOnInit();
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,desc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // INIT
      comp.ngOnInit();

      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,desc', 'id']);
    });
  });
});
